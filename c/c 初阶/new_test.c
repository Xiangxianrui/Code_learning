#include <stdio.h>
int main()
{
    int a = 3;
    int b = -5;
    int c = a & b;
    //& 都为 1 则为 1，其他情况为 0
    //|，有一个为 1 则为 1
    //^，相同为 0，不相同则为 1
    printf("%d\n", c);
    // 3的源码      00000000000000000000000000000011
    /// 补码         00000000000000000000000000000011
    //-5 的源码     10000000000000000000000000000101
    //-5的补码      11111111111111111111111111111011
    // 按位与之后的结果00000000000000000000000000000011
    // 输出则为 3

    // 按位与的结果
    return 0;
}